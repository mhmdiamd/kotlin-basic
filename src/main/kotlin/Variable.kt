fun main() {
//    Variable Mutable and Immutable

//    Mutable
    var firstName = "ilham"
    var lastName = "darmawan"
    firstName = "ilham updated"
    println("$firstName $lastName")

//    Immutable Variable
    val iFirstName = "Ilham"
    val iLastName = "Darmawan imutable"
    println("${iFirstName} ${iLastName}")
}